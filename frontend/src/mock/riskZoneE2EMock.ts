import type {
  RiskCandidateE2E,
  RiskPipelineSummary,
  RiskRuleThresholdSummary,
  RiskSuppressionSummary,
} from '../types/risk'

type CandidateSeed = Omit<
  RiskCandidateE2E,
  | 'riskScoreRule'
  | 'riskScoreMl'
  | 'riskScoreHybrid'
  | 'finalRiskScore'
  | 'rankingScore'
  | 'riskLabelRule'
  | 'riskLabelHybrid'
  | 'riskLabelFinal'
  | 'topTag'
>

function asRiskLabel(score: number): RiskCandidateE2E['riskLabelFinal'] {
  if (score >= 0.82) return 'high'
  if (score >= 0.56) return 'medium'
  return 'low'
}

function asTopTag(rank: number): RiskCandidateE2E['topTag'] {
  if (rank <= 5) return 'top5'
  if (rank <= 10) return 'top10'
  if (rank <= 20) return 'top20'
  return null
}

function makeCandidate(
  seed: CandidateSeed,
  scoreRule: number,
  scoreMl: number,
  rankingScore: number,
): RiskCandidateE2E {
  const riskScoreHybrid = Math.min(0.999, scoreRule * 0.58 + scoreMl * 0.42)
  const finalRiskScore = Math.min(0.999, riskScoreHybrid * 0.75 + rankingScore * 0.25)
  return {
    ...seed,
    riskScoreRule: scoreRule,
    riskScoreMl: scoreMl,
    riskScoreHybrid,
    finalRiskScore,
    rankingScore,
    riskLabelRule: asRiskLabel(scoreRule),
    riskLabelHybrid: asRiskLabel(riskScoreHybrid),
    riskLabelFinal: asRiskLabel(finalRiskScore),
    topTag: asTopTag(seed.rankGlobal),
  }
}

